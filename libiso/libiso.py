def ensure_uefi_bridge(cache_dir='.'):
    ''' Downloads the UEFI bridge image if it's not available '''

    local_path = os.path.join(cache_dir, UEFI_BRIDGE_NAME)
    
    if not os.path.exists(local_path):
        print(f'Downloading UEFI:NTFS bridge to {local_path}...')

        import urllib.request, urllib.error
        try:
            urllib.request.urlretrieve(UEFI_BRIDGE_DOWNLOAD_URL, local_path)
        except urllib.error.URLError as e:
            raise RuntimeError(f'Failed to download UEFI bridge: {e}')
            
    return local_path


def generate_dbx_rust_arrays(dbx_json_url: str = DBX_REVOCATION_URL, output_path = '../src/dbx.rs'):
    ''' Generates static arrays in Rust of revoked signatures, to allow fast verification '''

    import urllib.request, urllib.error, json, inspect

    res = urllib.request.urlopen(dbx_json_url).read().decode()
    data = json.loads(res)

    flat_hashes = set()
    for arch, images in data.get('images', {}).items():
        for image in images:
            flat_hash = image.get('flatHash', '').strip().upper()
            flat_hashes.add(flat_hash)
    
    if '' in flat_hashes: flat_hashes.remove('')
    sorted_hashes = sorted(list(flat_hashes))

    with open(output_path, 'w') as f:

        f.write(f'// -- Generated by libiso.{inspect.currentframe().f_code.co_name}()\n')
        f.writelines('\n'.join(
'''
pub const TRUSTED_MS_CA_THUMBPRINTS: &[&str] = &[
    'AEFC5FBBBE055D8F8DAA585473499417AB5A5272', 
    '81AA6B3244C935BCE0D6628AF39827421E32497D',
    'A92902398E16C49778CD90F99E4F9AE17C55AF50',
    '13ADBF4309BD82709C8CD54F316ED522988A1BD4E', 
];
'''.split('\n')))
        f.write(f'\n\n// Via -\n')
        f.write(f'// {DBX_REVOCATION_URL}\n\n')
        f.write('pub const DBX_HASHES: &[&str] = &[\n')
        for h in sorted_hashes:
            f.write(f'''    '{h}',\n''')
        f.write('];\n')

    print(f'Extracted {len(sorted_hashes)} flat hashes to {output_path}')


def extract_iso_label(iso_path: str) -> str:
    ''' 
    Extracts the Volume Identifier directly from an ISO 9660 image's 
    Primary Volume Descriptor (PVD).
    '''

    try:
        with open(iso_path, 'rb') as f:
            # ISO 9660 Volume Descriptors start at Sector 16 (16 * 2048 bytes)
            f.seek(16 * 2048)
            pvd = f.read(2048)
            
            # Check if it's a valid PVD: Type must be 1, ID must be 'CD001'
            if pvd[0] == 1 and pvd[1:6] == b'CD001':
                # The Volume Identifier is 32 bytes long, starting at offset 40
                raw_label = pvd[40:72]
                
                # Decode and strip trailing spaces and null bytes
                label = raw_label.decode('utf-8', errors='ignore').rstrip('\x00 ')
                if label:
                    return label
    except Exception as e:
        print(f'Warning: Failed to extract ISO label: {e}')
        
    return 'LIBISO_USB' # Fallback


def generate_disk_layout_ascii(device_path: str) -> str:
    ''' Generates a human-readable ASCII map of a GPT-formatted drive '''

    try:
        with open(device_path, 'rb') as f:
            f.seek(0, os.SEEK_END)
            total_sectors = f.tell() // 512

            # Read Primary GPT Header
            f.seek(512)
            hdr = f.read(512)
            if hdr[:8] != b'EFI PART':
                return '[!] Drive is not GPT formatted or header is missing.'
                
            _, _, _, _, _, _, bak_lba, first_usable, last_usable, _, part_start, num_parts, part_size, _ = struct.unpack('<8sIIIIQQQQ16sQIII', hdr[:92])
            
            # Read Partitions
            f.seek(part_start * 512)
            partitions = []
            for _ in range(min(num_parts, 4)): # Check first 4 slots
                p_data = f.read(part_size)
                type_guid, uniq_guid, p_start, p_end, attrs, name_utf16 = struct.unpack('<16s16sQQQ72s', p_data)
                if uniq_guid == b'\x00' * 16:
                    continue # Empty slot
                
                name = name_utf16.decode('utf-16le').rstrip('\x00')
                size_mb = ((p_end - p_start + 1) * 512) / (1024 * 1024)
                partitions.append((p_start, p_end, size_mb, name))

            # Draw the UI (Expanded Middle Column)
            lines = [
                '┌───────────────┬───────────────┬─────────────────────────────┐',
                '│                  libiso Flash Drive Layout                  │',
                '├───────────────┼───────────────┼─────────────────────────────┤',
                '│    Sector     │     Size      │         Description         │',
                '├───────────────┼───────────────┼─────────────────────────────┤',
                '│ LBA 0         │       512 B   │ Protective MBR (Type 0xEE)  │',
                '│ LBA 1         │       512 B   │ Primary GPT Header          │',
                '│ LBA 2-33      │        16 KB  │ Primary Partition Array     │',
                '│ LBA 34-2047   │      1007 KB  │ 1 MiB Alignment Padding     │',
                '├───────────────┼───────────────┼─────────────────────────────┤'
            ]
            
            for i, (p_start, p_end, size_mb, name) in enumerate(partitions):
                lines.append(f'│ LBA {p_start:<10}│               │ [Partition {i+1}]               │')
                lines.append(f'│       ...     │ {size_mb:>9.1f} MB  │ {name:<27} │')
                lines.append(f'│ LBA {p_end:<10}│               │                             │')
                if i < len(partitions) - 1:
                    lines.append('├───────────────┼───────────────┼─────────────────────────────┤')
                
            lines.extend([
                '├───────────────┼───────────────┼─────────────────────────────┤',
                f'│ LBA {last_usable+1:<10}│        16 KB  │ Backup Partition Array      │',
                f'│ LBA {bak_lba:<10}│       512 B   │ Backup GPT Header           │',
                '└───────────────┴───────────────┴─────────────────────────────┘'
            ])
            
            return '\n'.join(lines)
            
    except Exception as e:
        return f'[!] Failed to generate disk map: {e}'


def _check_gpt_corruption(device_path):
    ''' Aux diagnostic function '''
    
    import zlib

    if not os.path.exists(device_path):
        print(f'Cannot find {device_path}')
        return

    with open(device_path, 'rb') as f:
        # Check Primary GPT Header (LBA 1)
        f.seek(512)
        gpt_hdr = f.read(512)
        sig = gpt_hdr[:8]
        
        print('\n--- PRIMARY GPT (LBA 1) ---')
        if sig != b'EFI PART':
            print(f'[!] Signature Missing! Found: {sig.hex()}')
        else:
            print('''[+] Signature 'EFI PART' Found''')
            # Unpack the 92-byte header
            rev, hdr_size, crc, _, cur_lba, backup_lba, first_usable, last_usable, guid, part_start, num_parts, part_size, part_crc = struct.unpack('<IIIIQQQQ16sQIII', gpt_hdr[8:92])
            
            print(f'    Current LBA: {cur_lba}')
            print(f'    Backup LBA:  {backup_lba}')
            print(f'    Header CRC:  {hex(crc)}')
            
            # To verify the CRC, we must zero out the 4-byte CRC field (bytes 16-19)
            zeroed_hdr = gpt_hdr[:16] + b'\x00\x00\x00\x00' + gpt_hdr[20:hdr_size]
            actual_crc = zlib.crc32(zeroed_hdr) & 0xFFFFFFFF
            print(f'    Expected CRC: {hex(actual_crc)}')
            
            if crc != actual_crc:
                print('''    [!] FATAL: CRC Mismatch! Bad CRC's''')
            elif crc == 0:
                print('''    [!] FATAL: CRC is 0x0! CRC calculation wasn't made''')
            else:
                print('    [+] CRC Matches!')

        # Check Backup GPT Header (Last LBA)
        f.seek(0, os.SEEK_END)
        total_sectors = f.tell() // 512
        backup_offset = (total_sectors - 1) * 512
        
        print(f'\n--- BACKUP GPT (LBA {total_sectors - 1}) ---')
        f.seek(backup_offset)
        backup_hdr = f.read(512)
        
        if backup_hdr[:8] != b'EFI PART':
            print(f'[!] Backup Signature Missing! The Rust crate wrote it to the wrong place.')
        else:
            print(f'[+] Backup Signature Found!')



def _progress_bar(written: int, total: int, prefix: str = 'Progress'):

    percent = (written / total) * 100 if total > 0 else 0
    bar_len = 40
    filled = int(bar_len * written / total) if total > 0 else 0
    bar = '█' * filled + '-' * (bar_len - filled)
    
    # \r resets the cursor to the beginning of the line to overwrite it
    sys.stdout.write(f'\rProgress: |{bar}| {percent:.1f}% ({written}/{total} bytes)')
    sys.stdout.flush()


## -- Python wrappers

def write_image_iso(
    image_path: str,
    device_path: str,
    has_large_file: bool,
    usb_label: str,
    partition_scheme: str = 'gpt',
    uefi_ntfs_path: str = None,
    persistence_size_mb: int = None,
    verify_written: bool = False,
    unattend_xml_payload: str = None,
    target_arch: str = None
):
    ''' To avoid using the tempfile crate, which pulls windows-sys, we use a Python wrapper 
        and utilize it's built in tempfile
    '''

    ext4_temp_path = None
    
    if persistence_size_mb:
        fd, ext4_temp_path = tempfile.mkstemp(suffix='.ext4')
        os.close(fd)

    try:
        return _libiso.write_image_iso(
            image_path, device_path, has_large_file, usb_label, 
            partition_scheme, uefi_ntfs_path, persistence_size_mb, 
            ext4_temp_path, verify_written, unattend_xml_payload, target_arch
        )
    finally:
        if ext4_temp_path and os.path.exists(ext4_temp_path):
            os.remove(ext4_temp_path)


def burn_image(
    image_path: str, 
    device_path: str, 
    method: str = 'ISO',
    partition_scheme: str = None,     
    persistence_size_mb: int = None,
    verify_written: bool = False   
):
    ''' High level wrapper for libiso's burning logic '''

    if not os.path.exists(image_path):
        raise FileNotFoundError(f'Image not found: {image_path}')

    if (method := method.upper()) not in ('ISO', 'DD'):
        raise ValueError(f'Unknown method: {method}. Use iso or dd')

    iso_label = extract_iso_label(image_path)
    short_usb_label = iso_label[:11].replace(' ', '_').upper()
    print(f'''Detected ISO Label: '{iso_label}' -> Formatting USB as: '{short_usb_label}' ''')

    if method == 'DD':
        print(f'Starting raw DD write from {image_path} to {device_path}...')
        stream = _libiso.write_image_dd(image_path, device_path, verify_written)
        
        phase = 'DD Write'
        highest_written = 0
        
        for written, total in stream:
            # Check if Rust has entered the verification loop
            if written < highest_written:
                phase = 'Verifying'
                print('\nWrite complete! Starting bit-for-bit verification...')
                highest_written = 0
                
            highest_written = written
            _progress_bar(written, total, prefix=phase)
            
        print(f'\n{phase} Complete!')

    if method == 'ISO':
        print(f'Inspecting ISO: {image_path}')
        stats = _libiso.inspect_image(image_path)
        has_large_file = stats.has_large_file
        
        if partition_scheme is None:
            # If we detect UEFI, use GPT. Otherwise, fallback to MBR.
            # Using getattr is a safe way to check properties without crashing!
            partition_scheme = 'GPT' if getattr(stats.boot_info, 'supports_uefi', False) else 'MBR'

        uefi_path = ensure_uefi_bridge() if has_large_file else None

        print(f'Starting ISO filesystem extraction ({partition_scheme.upper()}) to {device_path}...')       
        stream = write_image_iso(
            image_path, 
            device_path, 
            has_large_file,
            iso_label,
            partition_scheme, 
            uefi_path, 
            persistence_size_mb
        )

    for written, total in stream:
        _progress_bar(written, total)
            
    print(f'\n{method} burning complete!') 
    return



