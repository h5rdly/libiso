def draw_progress(row: int, phase: str, written: int, total: int, text_col: int):

    width = 40
    percent = (written / total) if total > 0 else 0
    filled_chars = int(width * percent)
    
    bar = '█' * filled_chars + '░' * (width - filled_chars)
    sys.stdout.write(move_cursor(row, text_col) + f'\033[K{COLOR_WARNING}{phase:<15}:{COLOR_RESET} {COLOR_BAR}|{bar}|{COLOR_RESET} {percent*100:5.1f}%')
    sys.stdout.flush()


def tui_burn_worker(ui_queue: queue.Queue, iso_path: str, device_path: str, mode: str, verify: bool, stats):
    
    try:
        highest_written = 0
        phase = 'Writing'
        
        if mode == 'DD':
            ui_queue.put(('PHASE', 'Raw DD Write'))
            stream = _libiso.write_image_dd(iso_path, device_path, verify)
        else:
            ui_queue.put(('PHASE', 'ISO Extraction'))
            has_large_file = stats.has_large_file
            partition_scheme = 'GPT' if getattr(stats.boot_info, 'supports_uefi', False) else 'MBR'
            uefi_path = ensure_uefi_bridge() if has_large_file else None
            
            # Extract the label so the TUI also gets the pretty USB name
            iso_label = extract_iso_label(iso_path)
            short_usb_label = iso_label[:11].replace(' ', '_').upper()
            
            # Call the Python wrapper instead of the raw _libiso module
            stream = write_image_iso(
                iso_path, 
                device_path, 
                has_large_file, 
                iso_label,
                partition_scheme, 
                uefi_path, 
                persistence_size_mb=None, 
                verify_written=verify
            )
        
        for written, total in stream:
            if written < highest_written:
                phase = 'Verifying Data'
                ui_queue.put(('PHASE', phase))
                highest_written = 0
                
            highest_written = written
            ui_queue.put(('PROGRESS', written, total))
        
        ui_queue.put(('DONE', 'Burn and Verification Complete!'))

        if mode != 'DD':
            layout_map = generate_disk_layout_ascii(device_path)
            ui_queue.put(('LAYOUT', layout_map))

    except Exception as e:
        ui_queue.put(('ERROR', str(e)))


def tui_loop(iso_path: str, pre_mode: str = None, pre_verify: bool = None):

    try:
        stats = _libiso.inspect_image(iso_path)
    except Exception as e:
        print(f'Error inspecting ISO: {e}')
        return

    drives = _libiso.list_removable_drives()
    if not drives:
        print('No removable USB drives detected! Please insert a drive and try again.')
        return

    fd = sys.stdin.fileno()
    if os.name != 'nt':
        old_settings = termios.tcgetattr(fd)
        tty.setraw(fd)
    
    # Dynamic Wizard sequence
    # 0: Drive, 1: Mode, 2: Verify, 3: Confirm, 4: Burning
    step_sequence = [0]
    if pre_mode is None: step_sequence.append(1)
    if pre_verify is None: step_sequence.append(2)
    step_sequence.append(3) # Always show confirmation
    
    seq_idx = 0
    step = step_sequence[seq_idx]
    
    sel_drive = 0
    # Pre-fill selections if provided via CLI
    sel_mode = 1 if pre_mode == 'DD' else 0
    sel_verify = 1 if pre_verify else 0
    
    ui_queue = queue.Queue()
    is_burning = False
    current_phase = 'Waiting'
    
    try:
        sys.stdout.write(HIDE_CURSOR)
        
        while True:
            # calculate the center of the terminal for every frame
            cols, _ = os.get_terminal_size()
            base_col = max(1, (cols - 60) // 2)
            text_col = base_col + 2

            # Redraw the UI if we aren't burning
            if not is_burning:
                draw_wizard_ui(os.path.basename(iso_path), drives, step, sel_drive, sel_mode, sel_verify, stats, base_col, text_col)

            should_exit = False
            while not ui_queue.empty():
                msg = ui_queue.get()
                progress_row = 20 
                if msg[0] == 'PHASE':
                    current_phase = msg[1]
                elif msg[0] == 'PROGRESS':
                    draw_progress(progress_row, current_phase, msg[1], msg[2], text_col)
                elif msg[0] == 'ERROR':
                    sys.stdout.write(move_cursor(progress_row + 2, text_col) + f'\033[K{COLOR_DANGER}Error: {msg[1]}{COLOR_RESET}\n\n')
                    sys.stdout.flush()
                    should_exit = True

                elif msg[0] == 'DONE':
                    sys.stdout.write(move_cursor(6, 1) + '\033[J')
                    progress_row = 6
                    sys.stdout.write(move_cursor(progress_row, text_col) + f'\033[K{COLOR_SUCCESS}Success: {msg[1]}{COLOR_RESET}\n\n')
                    sys.stdout.flush()

                elif msg[0] == 'LAYOUT':  
                    map_lines = msg[1].strip('\n').split('\n')
                    progress_row = 7
                    for i, line in enumerate(map_lines):
                        sys.stdout.write(move_cursor(progress_row + i, text_col) + f'\033[K{line}')
                    progress_row += len(map_lines) + 2
                    sys.stdout.flush()
                    should_exit = True

            if should_exit:
                break

            key = read_key_nonblocking()
            if key == 'QUIT':
                break
            
            if not is_burning and step < 4:
                if key == 'UP':
                    if step == 0: sel_drive = max(0, sel_drive - 1)
                    elif step == 1: sel_mode = max(0, sel_mode - 1)
                    elif step == 2: sel_verify = max(0, sel_verify - 1)
                
                elif key == 'DOWN':
                    if step == 0: sel_drive = min(len(drives) - 1, sel_drive + 1)
                    elif step == 1: sel_mode = min(1, sel_mode + 1)
                    elif step == 2: sel_verify = min(1, sel_verify + 1)
                
                elif key == 'ENTER' and step < 3:
                    seq_idx += 1
                    step = step_sequence[seq_idx]
                
                elif step == 3: # Confirmation Step
                    if key == 'CONFIRM_NO':
                        seq_idx = 0 
                        step = step_sequence[seq_idx]
                    
                    elif key == 'CONFIRM_YES':
                        is_burning = True # Stop the wizard from redrawing and clear the lower half
                        step = 4 
                        target_device = drives[sel_drive].device_path
                        mode_str = 'DD' if sel_mode == 1 else 'ISO'
                        verify_bool = sel_verify == 1
                        
                        sys.stdout.write(move_cursor(18, text_col) + '\033[0J') 
                        sys.stdout.write(move_cursor(20, text_col) + f'{COLOR_TITLE}Starting deployment...{COLOR_RESET}')
                        sys.stdout.flush()
                        
                        threading.Thread(
                            target=tui_burn_worker, 
                            args=(ui_queue, iso_path, target_device, mode_str, verify_bool, stats), 
                            daemon=True
                        ).start()
                    
            if os.name != 'nt': time.sleep(0.01)

    finally:
        if os.name != 'nt':
            termios.tcsetattr(fd, termios.TCSADRAIN, old_settings)
        sys.stdout.write(SHOW_CURSOR)
        sys.stdout.write(move_cursor(24, 1)) 
        sys.stdout.flush()


